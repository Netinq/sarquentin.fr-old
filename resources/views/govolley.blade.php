        <h1>POLITIQUE DE CONFIDENTIALITÉ</h1>
        <h2>Définitions</h2>
        <p><b>L'Éditeur</b> : La personne, physique ou morale, qui édite les services de communication au public en ligne.
            <b>Le Site</b> : L'ensemble des sites, pages Internet et services en ligne proposés par l'Éditeur.
            <b>L'Utilisateur</b> : La personne utilisant le Site et les services.</p>
        <h2>1- Nature des données collectées</h2>
        <p>Dans le cadre de l'utilisation des Sites, l'Éditeur est susceptible de collecter les catégories de données suivantes concernant ses Utilisateurs :
            Données d'état-civil, d'identité, d'identification...
            Données relatives à la vie personnelle (habitudes de vie, situation familiale, hors données sensibles ou dangereuses)
            Données de localisation (déplacements, données GPS, GSM...</p>
        <h2>2- Communication des données personnelles à des tiers</h2>
        <p><b>Pas de communication à des tiers</b>
            Vos données ne font l'objet d'aucune communication à des tiers. Vous êtes toutefois informés qu'elles pourront être divulguées en application d'une loi, d'un règlement ou en vertu d'une décision d'une autorité réglementaire ou judiciaire compétente.</p>
        <h2>3- Information préalable pour la communication des données personnelles à des tiers en cas de fusion / absorption</h2>
        <p><b>Information préalable et possibilité d’opt-out avant et après la fusion / acquisition</b>
            Dans le cas où nous prendrions part à une opération de fusion, d’acquisition ou à toute autre forme de cession d’actifs, nous nous engageons à garantir la confidentialité de vos données personnelles et à vous informer avant que celles-ci ne soient transférées ou soumises à de nouvelles règles de confidentialité.</p>
        <h2>4- Agrégation des données</h2>
        <p><b>Agrégation avec des données non personnelles</b>
            Nous pouvons publier, divulguer et utiliser les informations agrégées (informations relatives à tous nos Utilisateurs ou à des groupes ou catégories spécifiques d'Utilisateurs que nous combinons de manière à ce qu'un Utilisateur individuel ne puisse plus être identifié ou mentionné) et les informations non personnelles à des fins d'analyse du secteur et du marché, de profilage démographique, à des fins promotionnelles et publicitaires et à d'autres fins commerciales.</p>
        <b>Agrégation avec des données personnelles disponibles sur les comptes sociaux de l'Utilisateur</b>
        <p>Si vous connectez votre compte à un compte d’un autre service afin de faire des envois croisés,
ledit service pourra nous communiquer vos informations de profil, de connexion, ainsi que toute
autre information dont vous avez autorisé la divulgation. Nous pouvons agréger les informations
relatives à tous nos autres Utilisateurs, groupes, comptes, aux données personnelles disponibles
sur l’Utilisateur.</p>
        <h2>5- Collecte des données d'identité</h2>
        <b>Utilisation d’un pseudonyme</b>
        <p>L’utilisation du Site nécessite une inscription sans identification préalable. Elle peut s’effectuer
sans que vous ne communiquiez de données nominatives vous concernant (nom, prénom,
adresse, etc). Vous pouvez utiliser le pseudonyme de votre choix.</p>
        <h2>6- Collecte des données d'identification</h2>
        <b>Utilisation de l'identifiant de l'utilisateur pour proposition de mise en relation et offres
commerciales</b>
        <p>Nous utilisons vos identifiants électroniques pour rechercher des relations présentes par
connexion, par adresse mail ou par services. Nous pouvons utiliser vos informations de contact
pour permettre à d'autres personnes de trouver votre compte, notamment via des services tiers
et des applications clientes. Vous pouvez télécharger votre carnet d'adresses afin que nous
soyons en mesure de vous aider à trouver des connaissances sur notre réseau ou pour
permettre à d'autres Utilisateurs de notre réseau de vous trouver. Nous pouvons vous proposer
des suggestions, à vous et à d'autres Utilisateurs du réseau, à partir des contacts importés de
votre carnet d’adresses. Nous sommes susceptibles de travailler en partenariat avec des
sociétés qui proposent des offres incitatives. Pour prendre en charge ce type de promotion et
d'offre incitative, nous sommes susceptibles de partager votre identifiant électronique.</p>
        <h2>7- Géolocalisation</h2>
        <b>Géolocalisation à des fins de fourniture du service</b>
        <p>Nous collectons et traitons vos données de géolocalisation afin de vous fournir nos services.
Nous pouvons être amenés à faire usage des données personnelles dans le but de déterminer
votre position géographique en temps réel. Conformément à votre droit d'opposition prévu par la
loi n°78-17 du 6 janvier 1978 relative à l'informatique, aux fichiers et aux libertés, vous avez la
possibilité, à tout moment, de désactiver les fonctions relatives à la géolocalisation.</p>
        <b>Géolocalisation à des fins de croisement</b>
        Nous collectons et traitons vos données de géolocalisation afin de permettre à nos services
d'identifier les points de croisement dans le temps et dans l'espace avec d'autres Utilisateurs du
service afin de vous présenter le profil des Utilisateurs croisés. Conformément à votre droit
d'opposition prévu par la loi n°78-17 du 6 janvier 1978 relative à l'informatique, aux fichiers et
aux libertés, vous avez la possibilité, à tout moment, de désactiver les fonctions relatives à la
géolocalisation. Vous reconnaissez alors que le service ne sera plus en mesure de vous
présenter de profil des autres Utilisateurs.</p>
        <b>Géolocalisation avec mise à disposition des partenaires pour référencement et agrégation
(avec opt-in)</b>
        <p>Nous pouvons collecter et traiter vos données de géolocalisation avec nos partenaires. Nous
nous engageons à anonymiser les données utilisées. Conformément à votre droit d'opposition
prévu par la loi n°78-17 du 6 janvier 1978 relative à l'informatique, aux fichiers et aux libertés,
vous avez la possibilité, à tout moment, de désactiver les fonctions relatives à la
géolocalisation.</p>
        <h2>8- Collecte des données du terminal</h2>
        <b>Aucune collecte des données techniques</b>
        <p>Nous ne collectons et ne conservons aucune donnée technique de votre appareil (adresse IP,
fournisseur d'accès à Internet...).</p>
        <h2>9- Cookies</h2>
        <b>Durée de conservation des cookies</b>
        Conformément aux recommandations de la CNIL, la durée maximale de conservation des
cookies est de 13 mois au maximum après leur premier dépôt dans le terminal de l'Utilisateur,
tout comme la durée de la validité du consentement de l’Utilisateur à l’utilisation de ces cookies.
La durée de vie des cookies n’est pas prolongée à chaque visite. Le consentement de
l’Utilisateur devra donc être renouvelé à l'issue de ce délai.</p>
        <b>Finalité cookies</b>
        <p>Les cookies peuvent être utilisés pour des fins statistiques notamment pour optimiser les
services rendus à l'Utilisateur, à partir du traitement des informations concernant la fréquence
d'accès, la personnalisation des pages ainsi que les opérations réalisées et les informations
consultées.
Vous êtes informé que l'Éditeur est susceptible de déposer des cookies sur votre terminal. Le
cookie enregistre des informations relatives à la navigation sur le service (les pages que vous
avez consultées, la date et l'heure de la consultation...) que nous pourrons lire lors de vos visites
ultérieures.</p>
        <b>Opt-in pour le dépôt de cookies</b>
        <p>Nous n'utilisons pas de cookies. Si nous devions en utiliser à l’avenir, vous en seriez informé
préalablement et auriez la possibilité de désactiver ces cookies.</p>
        <h2>10- Conservation des données techniques</h2>
        <b>Durée de conservation des données techniques</b>
        <p>Les données techniques sont conservées pour la durée strictement nécessaire à la réalisation
des finalités visées ci-avant.</p>
        <h2>11- Délai de conservation des données personnelles et d'anonymisation</h2>
        <p>Conservation des données pendant la durée de la relation contractuelle
Conformément à l'article 6-5° de la loi n°78-17 du 6 janvier 1978 relative à l'informatique, aux
fichiers et aux libertés, les données à caractère personnel faisant l'objet d'un traitement ne sont
pas conservées au-delà du temps nécessaire à l'exécution des obligations définies lors de la
conclusion du contrat ou de la durée prédéfinie de la relation contractuelle.
Conservation des données anonymisées au delà de la relation contractuelle / après la
suppression du compte</p>
